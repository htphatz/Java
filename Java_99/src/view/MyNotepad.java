    public void save(String fileName){
        PrintWriter wr = null;
        try {
            wr = new PrintWriter(fileName, StandardCharsets.UTF_8);
            String data = this.jTextArea.getText();
            wr.print(data);
            wr.flush();
            wr.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        } 
    }

