    private void jButton_openActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_openActionPerformed
        JFileChooser jfc = new JFileChooser();
        int returnVal = jfc.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION){
            File file = jfc.getSelectedFile();
            String fileName = file.getAbsolutePath();
            this.myNotepadModel.setTenFile(fileName);
            this.jLabel_path.setText(fileName);
            if (fileName.endsWith(".txt")){
                try {
                    List<String> allText = Files.readAllLines(file.toPath(), StandardCharsets.UTF_8);
                    String result = "";
                    for(String line : allText){
                        result += line;
                        result += "\n";
                    }
                    this.myNotepadModel.setNoiDung(result);
                    this.jTextArea.setText(result);
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton_openActionPerformed

    private void jButton_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveActionPerformed
        if (this.myNotepadModel.getTenFile().length() > 0) {
            save( this.myNotepadModel.getTenFile());
        }
        else {
            JFileChooser jfc = new JFileChooser();
            int returnVal = jfc.showSaveDialog(this);
            if (returnVal == JFileChooser.APPROVE_OPTION){
                try {
                    File file = jfc.getSelectedFile();
                    save(file.getCanonicalPath());
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton_saveActionPerformed

